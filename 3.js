// Rules for naming variables

// you cannot start with number 
// example:-
// 1value (invalid)
// value1 (valid)

var value1 = 2;
console.log(value1 ** 3); // ** is used for power/ ** 0.5 for square root


// you can only use _ or $ symbol 
//  first_name (valid)
// _firstname (valid)
var _firstname = "Niranjan"
console.log(_firstname)


var first_name = "Niranjan"
console.log(first_name)


// first$name (valid)
// $firstname(valid)
var $firstname = "Niranjan"
console.log($firstname)

